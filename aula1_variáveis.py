# Calcula média de notas

nota1 = int(input('\nDigite a primeira nota: '))
nota2 = int(input('\nDigite a segunda nota: '))
nota3 = int(input('\nDigite a terceira nota: '))
nota4 = int(input('\nDigite a quarta nota: '))

print('\n\nA média das notas é: {media}\n' .format(media=((nota1+nota2+nota3+nota4)/4)))